// BASIC TYPE

// string
let firstName = 'Duy Nghia Dev'
let lastName: string
lastName = 'Buck'
console.log(`firstName: ${firstName}, lastName: ${lastName}`)

// number
let age: number
age = 30
console.log(`age: ${age}`)

// boolean
let isValid = true
isValid = false
console.log(`isValid: ${isValid}`)
// isValid='hello'

// undefined, null
let k: undefined
let m: null
console.log(`k: ${k}`)
// console.log(`m: ${m}`);

// k='hello'
// m='null'

// any (nó sẽ nhận bất kì kiểu dữ liệu nào)
let x: any
x = 10
x = 'hello'
x = true
console.log(`x: ${x}`)
