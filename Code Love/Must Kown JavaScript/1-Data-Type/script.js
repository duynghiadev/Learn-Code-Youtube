// Kiểu dữ liệu nguyên thủy

// 1. Kiều dữ liệu undefined
console.log(typeof undefined)
let x
console.log(x)

// 2. Kiểu dữ liệu boolean
console.log(typeof true)
console.log(typeof false)

let isTrue = true
let isFalse = false
console.log(typeof isTrue)
console.log(typeof isFalse)

// 3. Kiểu dữ liệu number
console.log(typeof 123)
console.log(typeof 123.45)

let numberInt = 123
let numberFloat = 123.45
console.log(typeof numberInt)
console.log(typeof numberFloat)

// 4. Kiểu dữ liệu string
console.log(typeof 'duynghiadev')

// 5. Kiểu null: kiểu này nó rất đặt biệt
console.log(typeof null)
